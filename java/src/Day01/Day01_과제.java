package Day01;

public class Day01_과제 {
	
	public static void main(String[] args) {
		// 과제1 : 고양이 출력
		/*
		 *  \    /\
		 *   )  ( ')
		 * 	(  /  )
		 * 	 \(__)|
		 * 
		 */
		
		System.out.print( "\\    /\\\n" );
		System.out.print( " )  ( ')\n" );
		System.out.print( "(  /  )\n" );
		System.out.print( " \\(__)|\n" );
		
		// 과제2 : 개 출력
		/*
		 *  |\_/|
		 *  |q p|	/}
		 *  ( 0 )"""\
		 *  |"^"`	 |
		 *  ||_/=\\__|
		 */
		
		System.out.println("");
		
		System.out.print( "|\\_/|\n" );
		System.out.print( "|q p|   //\n" );
		System.out.print( "( 0 )\"\"\"\\\n" );
		System.out.print( "|\"^\"`    |\n" );
		System.out.print( "||_/=\\\\__|\n" );
		
	}
	
	
	

}
